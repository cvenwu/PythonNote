
# 导入模块

# 只有一个参数的range()
for i in range(12):
    print(i, end='\t')
print()

# 两个参数的range()
for i in range(0, 12):
    print(i, end='\t')
print()

# 三个参数的range()
for i in range(0, 12, 2):
    print(i, end='\t')
print()

# 三个参数的range(),第三个参数为负数
for i in range(12, 0, -2):
    print(i, end='\t')
print()
